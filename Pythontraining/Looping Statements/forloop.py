# for loop

# print 1...10 using for loop
for i in range(1, 11):
    print(i)

# print even numbers between 1 and 20
for j in range(2, 21, 2):
    print(j)

# print odd numbers between 1 and 20

for k in range(1, 20, 2):
    print(k)

# print 1 to 10 in descending order
for a in range(10, 0, -1):
    print(a)
