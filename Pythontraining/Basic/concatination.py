# Concatenation +

print(10 + 10)  # 20 valid
print(10.5 + 12.5)  # 22.5 valid
print(10 + 10.5)  # 20.5 valid
print('welcome' + 'python')  # welcomepython

# In python True = 1 & False = 0
print(True + 5)  # 6 valid
print(False + 5)  # 5 valid
print(True + True) # 2 valid
print(10 + 'welcome')  # Not valid because 2 values are different types
print(True + 'welcome')  # Not valid because 2 values are different types
