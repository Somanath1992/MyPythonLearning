# Arithmetic Operators
a = 5
b = 2

print(a + b)  # Addition
print(a - b)  # Subtraction
print(a * b)  # multiplication
print(a / b)  # Division
print(a // b)  # Division -quotient
print(a % b)  # Reminder
print(a ** b)  # Exponential / Square

# Relational Operators/ Comparison Operators
# > < >= <= != ==
# Always Returns boolean value True / False
x = 5
y = 10
print(x > y)  # False
print(x < y)  # True
print(x == y)  # False
print(x != y)  # True
print(x <= y)  # True
print(x >= y)  # False

# Logical Operators
# and or not
# works with boolean values
# Always returns boolean value
a = True
b = False

print(a and b)  # False
print(a or b)  # True
print(not a)  # False



