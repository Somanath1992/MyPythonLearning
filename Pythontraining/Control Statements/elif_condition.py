# Multiple Conditions using elif
week_no = int(input("Enter the value of week_no"))

if week_no == 1:
    print("Sunday")
elif week_no == 2:
    print("Monday")
elif week_no == 3:
    print("Tuesday")
elif week_no == 4:
    print("Wednesday")
elif week_no == 5:
    print("Thursday")
elif week_no == 6:
    print("Friday")
elif week_no == 7:
    print("Saturday")
else:
    print("Invalid week_no")
