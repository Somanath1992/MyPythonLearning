# Reverse the given List
# Approach 1: Using Reverse Method
myList = [10, 15, 20, 25, 40]
print("My List Before Reverse:", myList)
myList.reverse()
print("My List After Reverse:", myList)

# Approach 2: Using Slicing Technique
myList1 = [7, 6, 2, 14, 34]
print("My List Before Reverse:", myList1)
print("My List After Reverse:", myList1[::-1])
